    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        if (jTextField22.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a student id");
        } else {
            int sid = Integer.parseInt(jTextField22.getText());
            if (markSheet.isIdExist(sid)) {
                jTable5.setModel(new DefaultTableModel(null, new Object[]{
                    "ID", "Student ID", "Semester", "Course1", "Score1", "Course2", "Score2", "Course3", "Score3",
                    "Course4", "Score4", "Course5", "Score5", "Course6", "Score6", "Course7", "Score7", "Course8", "Score8", "Average"}));
                markSheet.getScoreValue(jTable5, sid);
                String gpa = String.valueOf(String.format("&.2f", markSheet.getGPA(sid)));
                jLabel24.setText("GPA:" + gpa);
            } else {
                JOptionPane.showMessageDialog(this, "No score found");
            }
        }
    }//GEN-LAST:event_jButton15ActionPerformed

